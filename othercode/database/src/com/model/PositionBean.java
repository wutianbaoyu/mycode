package com.model;

public class PositionBean {

	private int iPositionNo;
	private String nvPositionName;
	/**
	 * @return the iPositionNo
	 */
	public int getiPositionNo() {
		return iPositionNo;
	}
	/**
	 * @param iPositionNo the iPositionNo to set
	 */
	public void setiPositionNo(int iPositionNo) {
		this.iPositionNo = iPositionNo;
	}
	/**
	 * @return the nvPositionName
	 */
	public String getNvPositionName() {
		return nvPositionName;
	}
	/**
	 * @param nvPositionName the nvPositionName to set
	 */
	public void setNvPositionName(String nvPositionName) {
		this.nvPositionName = nvPositionName;
	}
}
